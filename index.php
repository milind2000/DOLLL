    <h1> About DOLLL </h1>
	<p style="text-align:justify;"> The department provides individuals the opportunities to learn at any stage of ones life. In various context and situtions, through all formal, non-formal and formal style of education. It is a pedagogical method, which is generally acieved through distance education, e-learning, correspondance education, continuing education. It includes educational programmess to improve and add new qualification, improve the existing skills or acquiring new skills. Institutions and organisations are making provisions to impart training and refresher courses for the employees to update their knowledge and skills to compete in this competitive world. The retiered people are also provided learning opportunities to utilize leisure time productivly. The acceleration of scientific and technological development has made life long learning very important. The time span for primary, secondary and tertiary education is  hardly sufficient to acquire a proffesional carrer, which will last for rest of his life. Life long learning has become a fundamental goal of recent education policies, often advocated as a way to acheive socioeconomic development and as a tool for promoting "Knowledge-Based" society. The UGC in its XII plan guidelines on Life Long Learning and extension strongly advocates that education does not end with schooling but is a lifelong process. The life long education as a cherished goal of educational process supposes Universal literacy, provision of opportunities for youth/housewives, agriculture and industrial workers, and proffesionals.	</p>
