<h3>FOREWORD</h3>

<p>One of the important milestones in the history of Devi Ahilya University, Indore, was starting a prestigious skill based programs from 2014.  The University Grants Commission (UGC) has launched a scheme on skills development based higher education as part of university education, leading to Bachelor of Vocation (B.Voc.) Degree has the options with multiple exits such as Diploma/Advanced Diploma under the NSQF. The B.Voc. programme is focused on universities and colleges providing undergraduate studies which would also incorporate specific job roles and their NOSs along with broad based general education. This would enable the graduates completing B.Voc. to make a meaningful participation in accelerating India’s economy by gaining appropriate employment, becoming entrepreneurs and creating appropriate  knowledge.</p>
<p>Space for residential and commercial places is shrinking due to  rapidurbanisation. This anomalous situation calls for professional help to make the living and working space more comfortable, beautiful and practical. Now-a-days people have become more and more particular about the designs and the layout of the living or working area. This has led to an increasing requirement for professionals in interior designing.</p>
<p>Fashion technology is the applied art devoted to the designing of clothing and lifestyle accessories. This art is influenced by cultural and social attitudes and has evolved over time and place. Fashion designing has come a long way from the designer clothes worn by the royalty of the ancient world to the haute couture products of the present. Along with regular classroom teachings the curriculum also involves a lot of field work live projects and market exposer thus providing the Students Sound technical knowledge and practical Know how’s of and good confidence because of this strong knowledge base.   </p>
<p>Artistic items have huge demand both within the country and outside. It is a small scale industry which is not capital intensive but creates large number of jobs too. People equipped with the talent of making traditional or artistic items can easily earn their livelihood. Despite the fact that there is a great demand of traditional artistic products today there is a dearth of people who are equipped with artistic skills to make them. With the dream of skill education reaching every home regardless of age, marks, time limits and anything that comes as a hurdle in your development, we have initiated the idea of value added Short term courses under continuing education or lifelong learning programs. With the utmost desire to bring out the talent within you we organize courses which can provide a means of exposure, excitement and economic gains. These courses are the value additions that you make for your personal growth.</p><br> 
<p>While making these promises we remember to remind you that having senior citizen in the courses is a pleasure for us, we heartily welcome homemakers and students who are on the break in their education can join us. Anyone who wants to nurture their creative instincts, yes it’s your way ahead.</p>
